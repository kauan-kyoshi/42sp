int		ft_atoi(char *c)
{
	int i = 0;
	int num = 0;
	int negative = 1;

	if (c[i] == ' ')
		i++;
	if (c[i] == '-')
	{
		negative *= -1;
		i++;	
	}
	while (c[i] >= '0' && c[i] <= '9')
	{
		num = (num * 10) + (c[i] - '0');
		i++;
	}
	return (num * negative);
}
